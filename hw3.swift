// 1

var teaCollection: [String: Int] = ["Чёрный чай": 30, "Зелёный чай": 40, "Фруктовый чай": 50]

var queue = ["Чёрный чай", "Зелёный чай", "Чёрный чай", "Чёрный чай", "Фруктовый чай", "Зелёный чай", "Фруктовый чай"]

var index = 1
for i in queue {
    let tee = teaCollection[i] ?? 0
    let stroka = String(index) + ". " + i + " за " + String(tee) + " руб."
    print(stroka)
    index += 1
}


