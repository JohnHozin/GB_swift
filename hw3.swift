// 3
func createNewArray (a: Int) -> [Int] {
    var newArray = [Int]()
    if a == 0 {
        return newArray
    }
    
    for i in 1...a {
        if i == 1 {
            newArray.append(i)
        } else {
            newArray.append(newArray[i-2]*2)
        }
    }
    return newArray
}

print(createNewArray(a: 6))
